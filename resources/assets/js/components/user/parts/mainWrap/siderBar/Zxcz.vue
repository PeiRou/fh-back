            if (hour < 6) {
                this.NowTime = "凌晨好"
            } else if (hour < 9) {
                this.NowTime = "早上好";
            } else if (hour < 12) {
                this.NowTime = "上午好";
            } else if (hour < 14) {
                this.NowTime = "中午好";
            } else if (hour < 17) {
                this.NowTime = "下午好";
            } else if (hour < 19) {
                this.NowTime = "傍晚好";
